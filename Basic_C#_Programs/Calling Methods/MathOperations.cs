using System;

namespace MathOperations
{
    public class Operations
    {
        // Method that performs addition operation on the given number
        public int Addition(int num1)
        {
            // Adds 10 to the given number
            return num1 + 14;
        }

        // Method that performs subtraction operation on the given number
        public int Subtraction(int num2)
        {
            // Subtracts 5 from the given number
            return num2 - 6;
        }

        // Method that performs multiplication operation on the given number
        public int Multiplication(int num3)
        {
            // Multiplies the given number by 2
            return num3 * 8;
        }
    }
}